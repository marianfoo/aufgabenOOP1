
public class Hello {

	public static void main(String[] args)
	{
		System.out.println("Hello World!\n"); // \n wird eine zusatzliche Zeile eingefugt
		System.out.println("Hello World!\n");
		System.out.print("Hello World!\t"); // bei \t wird ein Tabulator eingefugt
		System.out.print("Hello World!\t");
		System.out.print("Hello World!\n");
		System.out.print("Ich heisse: \"Max Mustermann\"\n"); // mit \" wird das Anfuhrungszeichen ausgesetzt
		System.out.print("und bin 19 1/2 Jahre alt\n");
		System.out.print("?xxxxxxxxxxxxxxxxxxxxxx!\n");
		System.out.println(3+4); //arithmetische Operatoren + - * /
		System.out.println(225 + 5);
		System.out.println(225 - 5);
		System.out.println(225 * 5);
		System.out.println(225 / 5);
		System.out.println(225 % 5); // % = Module; ist der Restwert, hier 0
		System.out.println(11 % 3); // Der Restwert ist hier 2
		
	}	
}

